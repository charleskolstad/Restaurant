CREATE PROCEDURE [dbo].[p_GroupUsers_GetByUserName]
(
	@UserName varchar(256)
)
AS
BEGIN
	-- cpk:<date>
	-- 
	SET NOCOUNT ON;

    SELECT [GroupUsersID]
		  ,[UserName]
		  ,[GroupUsers].[UserGroupID]
		  ,(SELECT[GroupName] FROM [dbo].[UserGroups] WHERE [UserGroupID] = [GroupUsers].[UserGroupID]) AS 'GroupName'
		  ,[GroupLevel]
		  ,[GroupUsers].[Active]
		  ,[GroupDescription]
	  FROM [dbo].[GroupUsers]
	INNER JOIN [UserGroups] ON
	[GroupUsers].[UserGroupID] = [UserGroups].[UserGroupID]
	 WHERE [UserName] = @UserName

END
GO

CREATE PROCEDURE [dbo].[p_GroupUsers_Update]
(
	 @UserName nvarchar(256)
    ,@UserGroups AS tblUserGroups READONLY
)
AS
BEGIN
	-- cpk:<date>
	-- 
	SET NOCOUNT ON;

	UPDATE [dbo].[GroupUsers]
	   SET [Active] = a.Active
	  FROM [GroupUsers] 
	INNER JOIN @UserGroups AS a ON
	[GroupUsers].[UserGroupID] = a.UserGroupID
	 WHERE [UserName] = @UserName --nvarchar(256)
    
END
GO

CREATE PROCEDURE [dbo].[p_UserInfo_Update]
(
	@UserName varchar(256),
	@ProfileImage varchar(1000) = NULL,
	@GroupUsers AS tblUserGroups READONLY
)
